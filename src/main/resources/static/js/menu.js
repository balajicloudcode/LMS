function menuRedirect(data){

		 if(data == "bookList" ){
			
			window.location = "bookList.html";
		}
		else if(data == "ReturnBook" ){
			
			window.location = "returnBook.html";
		}
		else if(data == "member" ){
			
			window.location = "member.html";
		}else{
			
			window.location = "mainContent.html";
		}
			

			


}